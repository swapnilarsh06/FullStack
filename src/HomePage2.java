    private void jButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton19ActionPerformed
        // TODO add your handling code here:
        DefaultTreeModel model=(DefaultTreeModel) jTree1.getModel();
        DefaultMutableTreeNode root=(DefaultMutableTreeNode) model.getRoot();
        String selected_list="<html><ul>";
        jLabel15.setText("");
        leaf_list.clear();
        leaf_count=0;
        try{
            TreePath[] paths = jTree1.getSelectionPaths();
            DefaultMutableTreeNode selectedNode;
            for (TreePath path : paths) {
                //for(int i=0;i<paths.length;i++){
                selectedNode=(DefaultMutableTreeNode) path.getLastPathComponent();
                if(selectedNode.isLeaf()){
                    leaf_count++;
                    selected_list=selected_list+"<li>"+selectedNode.toString()+"</li>";
                    leaf_list.add(selectedNode.toString());
                }
            }
            
            selected_list=selected_list+"</ul></html>";
            //for(String s : leaf_list)
            //    System.out.println(s);
            if (leaf_count==0)
                JOptionPane.showMessageDialog(null, "No tables/Views selected!", "Oops!", JOptionPane.INFORMATION_MESSAGE);
            else
                jLabel15.setText(selected_list);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "No tables/Views selected!", "Oops!", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton19ActionPerformed
    
