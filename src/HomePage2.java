    public void display_jTree(JTree tree, String user, String[] ta, String[] va){
        
        DefaultTreeModel model=(DefaultTreeModel) tree.getModel();
        DefaultMutableTreeNode root=(DefaultMutableTreeNode) model.getRoot();
        //System.out.println("The root is : "+root);
        root.removeAllChildren(); //this removes all nodes
        model.reload();
        
        root.setUserObject(user.toUpperCase());
        root.add(new DefaultMutableTreeNode("TABLES"));
        root.add(new DefaultMutableTreeNode("VIEWS"));
        model.reload();
        
        Enumeration children = root.children();
        while(children.hasMoreElements()){
            DefaultMutableTreeNode node = (DefaultMutableTreeNode) children.nextElement();
            if(node.toString().equals("TABLES")){
                for(int i=0;i<ta.length;i++)
                    node.add(new DefaultMutableTreeNode(ta[i]));
            }
            else if(node.toString().equals("VIEWS")){
                for (String va1 : va) {
                    node.add(new DefaultMutableTreeNode(va1));
                }
            }
        }
        model.reload();
    }
    
